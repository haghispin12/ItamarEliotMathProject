package com.example.calcultator;

public class Fruit {
    private String name;
    private int drawable;

    public Fruit(String name, int drawable){
        this.name = name;
        this.drawable = drawable;
    }

    public String getName(){
        return name;
    }

    public  String setName(String name){
        return name;
    }

    public int getDrawable(){
        return drawable;
    }

    public int setDrawable(int drawable){
        return drawable;
    }

}
